a = int(input("Введите километраж спортсмена в первый день"))
b = int(input("Введите необходимый результат спортсмена"))
day = 0
while a<b:
    a*=1.1
    day+=1
    print("Результат:")
    print(day, "-й день:", int(a), "км/ч")
    print("На", day, "-й день спорстмен достиг результата - не менее" , b, "км.")




